package io.jsonwebtoken.impl;

import io.jsonwebtoken.Header;
import io.jsonwebtoken.Jwt;
import io.jsonwebtoken.JwtVisitor;
import io.jsonwebtoken.io.Encoders;
import io.jsonwebtoken.lang.Assert;
import io.jsonwebtoken.lang.Objects;

public class DefaultJwt<H extends Header, P> implements Jwt<H, P> {
    private final H header;
    private final P payload;

    public DefaultJwt(H h10, P p10) {
        this.header = (Header) Assert.notNull(h10, "header cannot be null.");
        this.payload = Assert.notNull(p10, "payload cannot be null.");
    }

    public <T> T accept(JwtVisitor<T> jwtVisitor) {
        return jwtVisitor.visit((Jwt<?, ?>) this);
    }

    public boolean equals(Object obj) {
        if (obj == this) {
            return true;
        }
        if (!(obj instanceof Jwt)) {
            return false;
        }
        Jwt jwt = (Jwt) obj;
        if (!Objects.nullSafeEquals(this.header, jwt.getHeader()) || !Objects.nullSafeEquals(this.payload, jwt.getPayload())) {
            return false;
        }
        return true;
    }

    public P getBody() {
        return getPayload();
    }

    public H getHeader() {
        return this.header;
    }

    public P getPayload() {
        return this.payload;
    }

    public int hashCode() {
        return Objects.nullSafeHashCode(this.header, this.payload);
    }

    public final String toString() {
        return toStringBuilder().toString();
    }

    public StringBuilder toStringBuilder() {
        StringBuilder sb = new StringBuilder(100);
        sb.append("header=");
        sb.append(this.header);
        sb.append(",payload=");
        P p10 = this.payload;
        if (p10 instanceof byte[]) {
            sb.append(Encoders.BASE64URL.encode((byte[]) p10));
        } else {
            sb.append(p10);
        }
        return sb;
    }
}
